package com.pennant.backend.model.amtmasters;

import com.pennanttech.pennapps.core.model.AbstractWorkflowEntity;
import com.pennanttech.pennapps.core.model.LoggedInUser;

/**
 * Model class for the <b>Course table</b>.<br>
 *
 */
public class Course extends AbstractWorkflowEntity {
	private static final long serialVersionUID = 1L;
	private String courseName;
	private String courseDesc;
	private String lovValue;
	private Course befImage;
	private LoggedInUser userDetails;

	public boolean isNew() {
		return isNewRecord();
	}

	public Course() {
		super();
	}

	public Course(String id) {
		super();
		this.setId(id);
	}

	//Getter and Setter methods

	public String getId() {
		return courseName;
	}

	public void setId(String id) {
		this.courseName = id;
	}

	public String getCourseName() {
		return courseName;
	}

	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}

	public String getCourseDesc() {
		return courseDesc;
	}

	public void setCourseDesc(String courseDesc) {
		this.courseDesc = courseDesc;
	}

	public String getLovValue() {
		return lovValue;
	}

	public void setLovValue(String lovValue) {
		this.lovValue = lovValue;
	}

	public Course getBefImage() {
		return this.befImage;
	}

	public void setBefImage(Course beforeImage) {
		this.befImage = beforeImage;
	}

	public LoggedInUser getUserDetails() {
		return userDetails;
	}

	public void setUserDetails(LoggedInUser userDetails) {
		this.userDetails = userDetails;
	}
}
