package com.pennant.backend.model.systemmasters;

import com.pennanttech.pennapps.core.model.LoggedInUser;
import com.pennanttech.pff.core.model.AbstractWorkflowEntity;

/**
 * Model class for the <b>IdentityDetails table</b>.<br>
 * 
 */
public class IdentityDetails extends AbstractWorkflowEntity {

	private static final long serialVersionUID = -726560151676499162L;

	private String identityType;
	private String identityDesc;
	private boolean newRecord;
	private String lovValue;
	private IdentityDetails befImage;
	private LoggedInUser userDetails;

	public boolean isNew() {
		return isNewRecord();
	}

	public IdentityDetails() {
		super();
	}

	public IdentityDetails(String id) {
		super();
		this.setId(id);
	}

	// ******************************************************//
	// ****************** getter / setter *******************//
	// ******************************************************//

	public String getId() {
		return identityType;
	}
	public void setId(String id) {
		this.identityType = id;
	}

	public String getIdentityType() {
		return identityType;
	}
	public void setIdentityType(String identityType) {
		this.identityType = identityType;
	}

	public String getIdentityDesc() {
		return identityDesc;
	}
	public void setIdentityDesc(String identityDesc) {
		this.identityDesc = identityDesc;
	}

	public boolean isNewRecord() {
		return newRecord;
	}
	public void setNewRecord(boolean newRecord) {
		this.newRecord = newRecord;
	}

	public String getLovValue() {
		return lovValue;
	}
	public void setLovValue(String lovValue) {
		this.lovValue = lovValue;
	}

	public IdentityDetails getBefImage() {
		return this.befImage;
	}
	public void setBefImage(IdentityDetails beforeImage) {
		this.befImage = beforeImage;
	}

	public LoggedInUser getUserDetails() {
		return userDetails;
	}
	public void setUserDetails(LoggedInUser userDetails) {
		this.userDetails = userDetails;
	}
}
